                    <h1>Education/Employment</h1>

                    <p>
                        After receiving B.M. and M.A. degrees in music theory from the Eastman School of Music, Bruce Reiprich attended the University of Iowa, where he was awarded a
                        Ph.D. in music composition. Reiprich has also studied Schenkerian music theory privately with David Gagn&eacute;, a co-author of <em>The Analysis of Tonal Music</em>;
                        <em>A Schenkerian Approach</em>. A former faculty member of the Oberlin College Conservatory of Music and Wilkes University, Reiprich joined the Northern Arizona
                        University faculty in 1999 and served as coordinator of music theory and composition from 2002 to 2007. In 2003, he received the Teacher-of-the-Year Award from the
                        College of Fine Arts of NAU. He has been a judge for numerous composition contests including those sponsored by the Boston New Music Initiative, the Music Teachers National
                        Association and the Society of Composers. During the summer from 1998-2008 he served as composer-in-residence at the
                        Performing Arts Institute of Wyoming Seminary in Kingston, Pennsylvania.
                    </p>
