                    <h1>Music Theory Research</h1>

                    <p>
                        Bruce Reiprich's research on the music of Gy&ouml;rgy Ligeti has been published in <i>Perspectives of New Music</i> ("Transformation of Coloration
                        and Density in Gy&ouml;rgy Ligeti's <i>Lontano"</i>) and delivered at a Midwest Chapter meeting of the American Musicological Society. He presented
                        his study of Toru Takemitsu's <i>Garden Rain</i> at the Music of Japan Today Symposium hosted by Hamilton College in 1992. In 2004, he was invited by the
                        Martin Luther Universit&auml;t/Halle Wittenberg in Halle, Germany, to give a series of introductory workshops on Schenkerian music theory. He delivered a paper,
                        "Voice-Leading and Harmonic Background in Toru Takemitsu's <i>A Bird Came Down The Walk</i>," to the Society of Composers National and
                        Region V Conferences (2005), and the Music Theory Southeast Conference (2007).
                    </p>
